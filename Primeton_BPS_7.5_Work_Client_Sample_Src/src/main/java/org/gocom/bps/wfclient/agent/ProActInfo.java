package org.gocom.bps.wfclient.agent;




public class ProActInfo {
	
	private String itemID;
	
	private String itemType;
	
	private String itemName;
	
	private String accessType;
	
	private String proDefID;
	
	private String proFullName;

	public String getItemID() {
		return itemID;
	}

	public void setItemID(String itemID) {
		this.itemID = itemID;
	}

	public String getItemType() {
		return itemType;
	}

	public void setItemType(String itemType) {
		this.itemType = itemType;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public String getAccessType() {
		return accessType;
	}

	public void setAccessType(String accessType) {
		this.accessType = accessType;
	}

	public String getProDefID() {
		return proDefID;
		
	}

	public void setProDefID(String proDefID) {
		this.proDefID = proDefID;
	}

	public String getProFullName() {
		return proFullName;
	}

	public void setProFullName(String proFullName) {
		this.proFullName = proFullName;
	}
	
}
